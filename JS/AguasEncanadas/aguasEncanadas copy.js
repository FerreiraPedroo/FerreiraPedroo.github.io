class MatrizCelular {
  #cellElementSizeX;
  #cellElementSizeY;
  #cellElementPosX;
  #cellElementPosY;
  #cellIndexZMatriz;

  constructor(cellElementSizeX = 48, cellElementSizeY = 48, cellElementPosX, cellElementPosY, cellIndexZMatriz = 0) {
    this.#cellElementSizeX = cellElementSizeX;
    this.#cellElementSizeY = cellElementSizeY;
    this.#cellElementPosX = cellElementPosX;
    this.#cellElementPosY = cellElementPosY;
    this.#cellIndexZMatriz = cellIndexZMatriz;

  }

  getCellSize() {
    return { x: this.#cellElementSizeX, y: this.#cellElementSizeY };
  }
  getCellPosition() {
    return { x: this.#cellElementPosX, y: this.#cellElementPosY };
  }
  getCellIndexZMatriz() {
    return this.#cellIndexZMatriz;
  }

  setCellPosition({ x, y }) {
    this.#cellElementPosX = x;
    this.#cellElementPosY = y;
  }
  setCellIndexZMatriz(z) {
    this.#cellIndexZMatriz = z;
  }

}

