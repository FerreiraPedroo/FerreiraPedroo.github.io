function matrizCelularRenderHTML(_matrizCelularArray) {
  const matriz = _matrizCelularArray;
  const matrizObjects = _matrizCelularArray;
  const matrizHtml = document.getElementById("matriz-board");

  matriz.forEach((cellLine, indexCellLine) => {
    const cellLineElement = document.createElement("div");
    cellLineElement.setAttribute("id", indexCellLine);
    cellLineElement.setAttribute("class", "cell-line");

    cellLine.forEach((cell, indexCellColumn) => {
      const cellElement = document.createElement("div");
      cellElement.setAttribute("id", `${indexCellLine}-${indexCellColumn}`);
      cellElement.setAttribute("class", "cell");
      cellElement.style.zIndex = cell.cellIndexZMatriz;
      cellElement.innerText = `X:${indexCellLine}-Y:${indexCellColumn}`;

      cellElement.setAttribute("onclick", `cellLevelUpper(${indexCellLine},${indexCellColumn})`);

      const cellObject = new MatrizCelular(cell.cellElementSizeX, cell.cellElementSizeY, indexCellLine, indexCellColumn, cell.cellIndexZMatriz);
      matrizObjects[indexCellLine][indexCellColumn] = cellObject;
      cellLineElement.appendChild(cellElement);
    });

    matrizHtml.appendChild(cellLineElement);
  })

}
